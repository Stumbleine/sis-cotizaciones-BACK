package com.umss.dev.output;

public class ReportOutput {
	
	private String nameBusiness;
	private String areaBusiness;
	private String commentary;
	private Double totalQuotation;
	
	public String getNameBusiness() {
		return nameBusiness;
	}
	
	public void setNameBusiness(String nameBusiness) {
		this.nameBusiness = nameBusiness;
	}
	
	public String getAreaBusiness() {
		return areaBusiness;
	}
	
	public void setAreaBusiness(String areaBusiness) {
		this.areaBusiness = areaBusiness;
	}
	
	public String getCommentary() {
		return commentary;
	}
	
	public void setCommentary(String commentary) {
		this.commentary = commentary;
	}

	public Double getTotalQuotation() {
		return totalQuotation;
	}

	public void setTotalQuotation(Double totalQuotation) {
		this.totalQuotation = totalQuotation;
	}

}
